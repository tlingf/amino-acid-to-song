/* Normalize propensity values to [0,1] range */
function _normProp(prop) {
  const vals = Object.values(prop);
  const mn = Math.min(...vals), mx = Math.max(...vals);
  const r = mx - mn || 1;
  const out = {};
  for (const aa of AA_ORDER) out[aa] = (prop[aa] - mn) / r;
  return out;
}

const _NH = _normProp(HELIX_PROP);
const _NS = _normProp(SHEET_PROP);

/**
 * Return the 4 most likely next amino acids given the last one.
 * Blends bigram probability with structural propensity.
 */
function getTop4(lastAA) {
  if (!lastAA) return ['M', 'A', 'L', 'G'];
  const ri = AA_ORDER.indexOf(lastAA);
  if (ri < 0) return ['M', 'A', 'L', 'G'];
  const row = BG[ri];
  const scored = AA_ORDER.split('').map((aa, j) => ({
    aa,
    score: 0.6 * row[j] + 0.2 * _NH[aa] * 0.05 + 0.2 * _NS[aa] * 0.05
  }));
  scored.sort((a, b) => b.score - a.score);
  return scored.slice(0, 4).map(s => s.aa);
}
